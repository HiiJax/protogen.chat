<h1>About</h1>
<hr>
<p>
  protogen<span class="accent">.</span>chat is a selection of <a href="/services">web services</a> 
  hosted by <a href="https://hiijax.net">Jax</a>.
</p>
<p>
  The goal of protogen<span class="accent">.</span>chat is to be a safe and 
  connected space for the data of protogens (and protogen fans) on the web. If there's demand for 
  something that can be hosted here, I'd be thrilled to make it happen.
</p>
<p>
  Originally, the domain was purchased for a Matrix instance. Further into the process of 
  setting up Matrix though, I deemed it too complex for my backend to handle, and not user 
  friendly enough to invite loads of protogens into.
</p>
<p>Due to the .chat TLD, a big hope is to provide a Fluxer instance in the future, but that will require:</p>
<ul>
  <li>Fluxer being able to make use of a custom domain</li>
  <li>My setup working well with Fluxer</li>
  <li>Moderation being handled by someone other than Jax</li>
</ul>
<p>So we'll have to wait and see.</p>