<h1>About</h1>
<hr>
<h2>What is protogen<span class="accent">.</span>chat?</h2>
<p>
    Currently, protogen<span class="accent">.</span>chat is a <a href="/bluesky">
    Bluesky subdomain service</a> and <a href="/webring">webring</a>, hosted on Cloudflare Pages. 
    Originally, I (<a href="https://hiijax.net">Jax</a>, site owner) was influenced to seek 
    alternatives to Discord when they attempted to push a 
    <a href="https://kotaku.com/discord-palantir-peter-thiel-persona-age-verification-2000668951">
    Palantir-connected ID system</a> on their users. Matrix looked like a good contender. 
    It was recommended to use a dedicated domain for it, so that's what 
    protogen<span class="accent">.</span>chat was supposed to be for. Further into the process of 
    setting up Matrix though, I deemed it too complex for my backend to handle, and not user 
    friendly enough to invite loads of protogens into.
</p>
<h2>What will protogen<span class="accent">.</span>chat be?</h2>
<p>
    I would love for protogen<span class="accent">.</span>chat to be shaped by the community.
    If I hear demand for something I can provide through this domain, I'd be over the moon to
    make it happen. Given the .chat TLD, I do think that trying to provide social and especially
    real-time chat services here would be ideal, but I'm open to just about anything.
</p>
<p>My only currently future plans are a Fluxer instance, but that hinges on:</p>
<ul>
    <li>Fluxer being able to make use of a custom domain</li>
    <li>My setup working well with Fluxer</li>
    <li>Fluxer not being dogass</li>
    <li>People ACTUALLY trying to make a change rather than being lazy and using mechahitler slop platforms (unlikely)</li>
</ul>
<p>So right now I'm just waiting to see how things pan out :3</p>